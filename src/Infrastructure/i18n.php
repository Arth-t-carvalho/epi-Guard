/**
 * Tradução de itens vindos do Banco de Dados
 * Prioriza a coluna 'nome_en' se o idioma for 'en'
 */
function __db($item, string $key = 'nome'): string
{
    $lang = $_COOKIE['epiguard-lang'] ?? 'pt-br';

    if ($lang === 'en') {
        // Se for um array (resultado de fetch_assoc)
        if (is_array($item)) {
            return !empty($item[$key . '_en']) ? $item[$key . '_en'] : ($item[$key] ?? '');
        }
        // Se for um objeto (Entidade)
        if (is_object($item)) {
            $methods = [
                'get' . ucfirst($key) . 'En',
                'get' . ucfirst($key === 'nome' ? 'name' : $key) . 'En'
            ];
            foreach ($methods as $m) {
                if (method_exists($item, $m) && !empty($item->$m())) {
                    return $item->$m();
                }
            }

            $fallbacks = [
                'get' . ucfirst($key),
                'get' . ucfirst($key === 'nome' ? 'name' : $key)
            ];
            foreach ($fallbacks as $m) {
                if (method_exists($item, $m)) {
                    return $item->$m();
                }
            }
        }
    }

    // Fallback para o original
    if (is_array($item))
        return $item[$key] ?? '';
    if (is_object($item)) {
        $fallbacks = [
            'get' . ucfirst($key),
            'get' . ucfirst($key === 'nome' ? 'name' : $key)
        ];
        foreach ($fallbacks as $m) {
            if (method_exists($item, $m)) {
                return $item->$m();
            }
        }
    }

    return is_string($item) ? $item : '';
}